
public class CharactersTest {
	public static void main(String[] args) {
		char letter = 'A';
		System.out.println(letter);
		
		char value = 66;
		System.out.println(value);
		
		value = (char) (value + 1);
		System.out.println(value);
		
		String word = "welcome to the dark side";
		System.out.println(word);
		
		word = word + 2020;
		System.out.println(word);
	}
}
